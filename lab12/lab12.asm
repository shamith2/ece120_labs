; The table below represents an 8x16 font.  For each 8-bit extended ASCII
; character, the table uses 16 memory locations, each of which contains
; 8 bits (the high 8 bits, for your convenience) marking pixels in the
; line for that character.


; R0 is used for printing character
; R1 stores starting address
; R2 stores the letter to be printed
; R3 is used for calculating offset
; R4 is Counter 1 (also used as row counter)
; R5 is used for storing old R0 value before R0 is used to print a new character
; R6 is used as column counter

                    .ORIG x3000        ; PC set to x3000

                    AND R0,R0,#0       ; clearing R0
                    AND R1,R1,#0       ; clearing R1
                    AND R2,R2,#0       ; clearing R2
                    AND R3,R3,#0       ; clearing R3
                    AND R4,R4,#0       ; clearing R4
                    AND R6,R6,#0       ; clearing R6

                    ADD R4,R4,#15      ; Counter 1 initialized to 15

                    LEA R1,FONT_DATA   ; Storing starting address of FONT_DATA IN R1
	                LDI R2,RESULT      ; Storing the ascii value of letter to be printed in R2

OFFSET              ADD R3,R2,R3       ; Calculating offset to navigate through FONT_DATA
	                ADD R4,R4,#-1      ; to reach the address of the desired letter
	                BRzp OFFSET

                    ADD R1,R1,R3       ; Adding offset to initial address to reach desired address

                    AND R4,R4,#0       ; Clearing R4 since R4 was used before
		            ADD R4,R4,#15      ; Row Counter initialized to 15

LOAD                LDR R0,R1,#0       ; Load contents of address in R1 to R0
					AND R5,R5,#0       ; Clearing R5
					ADD R5,R5,R0       ; Storing R0 in R5
					AND R6,R6,#0       ; clearing R6
					ADD R6,R6,#8       ; initializing R6 to 8 because new row has started

BRANCH              ADD R0,R0,#0
					BRzp ZERO          ; read bit. if R0 is zero, go to ZERO
                    ADD R0,R0,#0
		            BRn ONE            ; if R0 is one, go to ONE

ADDN		        AND R0,R0,#0	   ; clearing R0 because it was used to print a new character
									   ; and no longer contains the contents of R1
					ADD R0,R0,R5       ; storing R5 in R0 since R5 contained the previous value of R0 
									   ; before R0 was used for printing
					ADD R0,R0,R0       ; left shifting bits in R0
				    AND R5,R5,#0	   ; clearing R5
					ADD R5,R0,R5       ; storing the new R0 value in R5
                    ADD R6,R6,#-1      ; used for priting R0, 8 times
                    BRp BRANCH
                    ADD R6,R6,#0
                    BRz INCREMENT

INCREMENT	        ADD R1,R1,#1       ; move to next address
		            BRnzp NEW_ROW      ; and move to next row

NEW_ROW   			ADD R4,R4,#-1
					BRzp ROW_FINISH
					ADD R4,R4,#0
		            BRn FINISH         ; if R4 < 0, halt the program (we have printed 16 rows)

ROW_FINISH	        LD R0,NEWLINE      ; load NewLine ASCII value
		            OUT                ; print new line char
					BRnzp LOAD

PRINT               OUT                ; displays the character in R0	
		            BRnzp ADDN         ; for reading next character

ZERO                LDI R0,ZERO_BIT    ; stores the character to be printed in R0
		            BRnzp PRINT        ; if bit is zero

ONE                 LDI R0,ONE_BIT     ; stores the character to be printed in R0
		            BRnzp PRINT        ; if bit is one

FINISH              HALT

